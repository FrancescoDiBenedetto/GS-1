function clickMenu() {
    if (menu.style.display == 'block') {
        menu.style.display = 'none'
    } else {
        menu.style.display = 'block'
    }
}
function clickPerfil() {
    if (perfil.style.display == 'block') {
        perfil.style.display = 'none'
    } else {
        perfil.style.display = 'block'
    }
}